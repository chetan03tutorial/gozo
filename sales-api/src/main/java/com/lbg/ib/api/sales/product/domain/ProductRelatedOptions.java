package com.lbg.ib.api.sales.product.domain;

public class ProductRelatedOptions {

    private String featureRelCode;
    private String featureCode;
    private String id;
    private String desc;
    private String code;
    private String name;
    private String type;
    private String value;

    /**
     * @return the featureRelCode
     */
    public String getFeatureRelCode() {
        return featureRelCode;
    }

    /**
     * @param featureRelCode
     *            the featureRelCode to set
     */
    public void setFeatureRelCode(String featureRelCode) {
        this.featureRelCode = featureRelCode;
    }

    /**
     * @return the featureCode
     */
    public String getFeatureCode() {
        return featureCode;
    }

    /**
     * @param featureCode
     *            the featureCode to set
     */
    public void setFeatureCode(String featureCode) {
        this.featureCode = featureCode;
    }

    /**
     * @return the id
     */
    public String getId() {
        return this.id;
    }

    /**
     * @param id
     *            the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @return the desc
     */
    public String getDesc() {
        return desc;
    }

    /**
     * @param desc
     *            the desc to set
     */
    public void setDesc(String desc) {
        this.desc = desc;
    }

    /**
     * @return the code
     */
    public String getCode() {
        return code;
    }

    /**
     * @param code
     *            the code to set
     */
    public void setCode(String code) {
        this.code = code;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name
     *            the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type
     *            the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the value
     */
    public String getValue() {
        return value;
    }

    /**
     * @param value
     *            the value to set
     */
    public void setValue(String value) {
        this.value = value;
    }

}
