package com.lbg.ib.api.sales.party;

public class CardOrder {

    private String name;
    private String addressLine1;
    private String addressLine2;
    private String addressLine3;
    private String postCode;
    private String sortCodeAccount;
    private String plasticNarr;

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the addressLine1
     */
    public String getAddressLine1() {
        return addressLine1;
    }

    /**
     * @param addressLine1 the addressLine1 to set
     */
    public void setAddressLine1(String addressLine1) {
        this.addressLine1 = addressLine1;
    }

    /**
     * @return the addressLine2
     */
    public String getAddressLine2() {
        return addressLine2;
    }

    /**
     * @param addressLine2 the addressLine2 to set
     */
    public void setAddressLine2(String addressLine2) {
        this.addressLine2 = addressLine2;
    }

    /**
     * @return the postCode
     */
    public String getPostCode() {
        return postCode;
    }

    /**
     * @param postCode the postCode to set
     */
    public void setPostCode(String postCode) {
        this.postCode = postCode;
    }

    /**
     * @return the sortCodeAccount
     */
    public String getSortCodeAccount() {
        return sortCodeAccount;
    }

    /**
     * @param sortCodeAccount the sortCodeAccount to set
     */
    public void setSortCodeAccount(String sortCodeAccount) {
        this.sortCodeAccount = sortCodeAccount;
    }

    /**
     * @return the plasticNarr
     */
    public String getPlasticNarr() {
        return plasticNarr;
    }

    /**
     * @param plasticNarr the plasticNarr to set
     */
    public void setPlasticNarr(String plasticNarr) {
        this.plasticNarr = plasticNarr;
    }

    /**
     * @return the addressLine3
     */
    public String getAddressLine3() {
        return addressLine3;
    }

    /**
     * @param addressLine3 the addressLine3 to set
     */
    public void setAddressLine3(String addressLine3) {
        this.addressLine3 = addressLine3;
    }
}
